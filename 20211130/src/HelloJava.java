
public class HelloJava {

	// 슬래시 2개를 덧붙인 이후에 적히는 내용은 주석.
	// 주석은 코드에 대한 설명을 덧붙이는데 사용.

	// main 이라고 적힌 하단의 중괄호 열리부분 ~ 닫힌부분 사이의 코드만 실행됩니다. 
	public static void main(String[] args) {
		// "적힌내용" 을 콘솔이라고 불리는 창에 찍어줍니다. 
		System.out.println("Hello World");
		System.out.println("이름은 홍길동입니다.");
		System.out.println("전 자바를 배웁니다 .");
		System.out.println();
		System.out.println("오늘은 \"월요일\" 입니다.");
		System.out.println("행복지수는 120%입니다.");
	}

}
