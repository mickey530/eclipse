
public class variable {
	public static void main(String[] args) {
		// 변수는 일종의 박스입니다.
		// 이 박스에는 넣을 물건의 종류와, 박스명칭을 부여해야 합니다.
		
		//아래는 "정수" 라는 물건은 a라는 이름의 박스에 담을 예정이라는 의미입니다. (아직 안넣음)
		
		int a;
		
		// a 박스에 5를 집어넣습니다. 
		
		a = 5;
		
		// a박스에 들어있는 값을 콘솔에 찍어봅시다. 
		System.out.println(a);
		
		// 정수를 담을 수 있는 A 라는 박스를 생성해주요.
		// A 박스에 10을 넣어주세요.
		// A 박스에 들어있던 값을 콘솔에 찍어보세요.
		
		int A;
		A = 10;
		System.out.println(A);
		
		System.out.println(a+A);
		
		// 없는 변수는 호출이 안됩니다. 
		
	}
}
